const calculator = {
	plus: function (a, b) {
		console.log(a + b);
	},
	minus: function (a, b) {
		console.log(a - b);
	},
	times: function (a, b) {
		console.log(a * b);
	},
	divide: function (a, b) {
		console.log(a / b);
	},
	power: function (a) {
		console.log(a * a);
	},
};

calculator.plus(5, 8);
calculator.minus(5, 8);
calculator.times(5, 8);
calculator.divide(5, 8);
calculator.power(5, 8);
